#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*curr;

	if (lst == NULL)
		return ;
	while (*lst != NULL)
	{
		curr = *lst;
		*lst = curr->next;
		del(curr->content);
		free(curr);
	}
}
